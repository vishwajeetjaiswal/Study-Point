			<h2>Java Introduction</h2>
				<p>Java is an object-oriented, class-based, concurrent, secured and general-purpose computer-programming language.</p>
				<P>Java is a platform independent.</P>
				<p>Any hardware or software environment in which a program runs, is known as a platform. Since Java has a runtime environment (JRE) and API, it is called a platform.</P>
			<h2>JAVA: History</h2>
				<p>Java initial work started in 1990 by Sun Microsystems engineer Patrick Naughton as a part of the Stealth Project.</p>
				<p>The Stealth Project soon changed to the Green Project, with Mike Sheridan and James Gosling joining the ranks, and the group began developing new technology for programming next-generation smart appliances						</p>
				<p><strong>James Gosling, Mike Sheridan,</strong> and <strong>Patrick Naughton</strong> initiated the Java language project in June 1991. </p>
				<p>Gosling attempted to modify and extend C++ (a development he refers to as "C++ ++ --"), but quickly abandoned this approach in favour of creating an entirely new language.		</p>
				<p>Firstly, it was called "Greentalk" by James Gosling, and file extension was .gt. After that, it was called Oak, named after the tree that stood outside his office.</p>
				<p>Originally designed for small, embedded systems in electronic appliances like set-top boxes. Then incorporate some changes based on emergence of World Wide Web, which demanded portable programs.	</p>
				<p>In 1995, Oak was renamed as "Java" because it was already a trademark by Oak Technologies.</p>
				<p>The first publicly available version of Java (Java 1.0) was released in 1995. </p>
				<p>Sun Microsystems was acquired by the Oracle Corporation in 2010. In 2006 Sun started to make Java available under the GNU General Public License (GPL). Oracle continues this project called OpenJDK.  </p>
